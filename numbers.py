def nums_100_999(a, b):
    """
    Returns a list of [digit-intiger, case, digit-string] for the provided range between 100-999.
    Uses the pythonic range input.
    """

    def _hundreds(n):
        n1 = int(str(n)[0])
        f = "f_at_af"
        if n1 == 1:
            f = "f_et_hk_nf"
            num = f"{base[n1,f]} hundrað"

        elif n1 in [2, 3, 4]:
            f = "f_ft_hk_nf"
            num = f"{base[n1,f]} hundruð"
        else:
            f = "f_at_af"
            num = f"{base[n1,f]} hundruð"

        return f, num

    numbers = []
    for n in range(a, b):
        if n % 100 == 0:
            f, n1 = _hundreds(n)
            numbers.append([n, f, n1])
        else:
            n1 = int(str(n)[0] + "00")
            n2 = n - n1
            num = _hundreds(n1)[1]
            if n2 == 1:
                f = "f_et_kk_nf"
                num += f" og {base[n2,f]}"
            elif n2 in [2, 3, 4]:
                f = "f_ft_kk_nf"
                num += f" og {base[n2,'f_ft_kk_nf']}"
            elif n2 in [x for x in range(5, 20)]:
                f = "f_at_af"
                num += f" og {base[n2,'f_at_af']}"
            else:
                n2 = nums_20_99(n2, n2 + 1)[0]
                f = n2[1]
                num += f" {n2[2]}"
            numbers.append([n, f, num])
    return numbers


