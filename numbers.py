def save_numbers_tsv(numbers: list) -> None:
    for indx, line in enumerate(numbers):
        numbers[indx] = [str(x) for x in line]
    with open("numbers.tsv", "w") as f_out:
        for line in numbers:
            f_out.write("\t".join(line) + "\n")


